package app.android.sqlitepwpb;

public class PersonBean {
    String nama;
    int age;

    public PersonBean() {}

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
